# Values before operation
a = 15
b = 10

print("Before Comparison: a =", a, ", b =", b)

# Comparison operations
print("a == b:", a == b)  # False
print("a != b:", a != b)  # True
print("a > b:", a > b)    # True
print("a < b:", a < b)    # False
print("a >= b:", a >= b)  # True
print("a <= b:", a <= b)  # False

# Values remain unchanged after comparison
print("After Comparison: a =", a, ", b =", b)
