# Values before operation
a = 10
b = 3

print("Before Operation: a =", a, ", b =", b)

# Arithmetic Operations
print("Addition (a + b) =", a + b)      # 10 + 3 = 13
print("Subtraction (a - b) =", a - b)   # 10 - 3 = 7
print("Multiplication (a * b) =", a * b) # 10 * 3 = 30
print("Division (a / b) =", a / b)      # 10 / 3 = 3.3333
print("Floor Division (a // b) =", a // b) # 10 // 3 = 3 (Removes decimal part)
print("Modulus (a % b) =", a % b)       # 10 % 3 = 1 (Remainder)
print("Exponentiation (a ** b) =", a ** b) # 10^3 = 1000

# Values after operation remain unchanged as arithmetic operators do not modify variables
print("After Operation: a =", a, ", b =", b)
