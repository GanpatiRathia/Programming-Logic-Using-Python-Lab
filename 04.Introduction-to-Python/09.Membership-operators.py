# Values before operation
my_list = [10, 20, 30, 40]

print("Before Membership Operation: my_list =", my_list)

# Membership operations
print("10 in my_list:", 10 in my_list)   # True
print("50 in my_list:", 50 in my_list)   # False
print("30 not in my_list:", 30 not in my_list)  # False

# Values remain unchanged after membership operations
print("After Membership Operation: my_list =", my_list)
