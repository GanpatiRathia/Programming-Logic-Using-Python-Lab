# Initial value
x = 5
print("Initial x =", x)

# Using assignment operators
x += 3  # x = x + 3
print("After x += 3:", x)  # 5 + 3 = 8

x -= 2  # x = x - 2
print("After x -= 2:", x)  # 8 - 2 = 6

x *= 4  # x = x * 4
print("After x *= 4:", x)  # 6 * 4 = 24

x /= 3  # x = x / 3
print("After x /= 3:", x)  # 24 / 3 = 8.0

x //= 2  # x = x // 2
print("After x //= 2:", x)  # 8 // 2 = 4.0

x **= 2  # x = x ** 2
print("After x **= 2:", x)  # 4^2 = 16.0

x %= 5  # x = x % 5
print("After x %= 5:", x)  # 16 % 5 = 1.0
